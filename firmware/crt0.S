.section .text
.global _start

_start:
    # ---------------------------------------------------------
    # 1. Initialize Stack Pointer
    # Memory Depth = 1024 words = 4096 bytes = 0x1000
    # Stack grows DOWN, so we set SP to the very top.
    # ---------------------------------------------------------
    li sp, 0x00001000  
    
    # ---------------------------------------------------------
    # 2. Jump to C Code
    # ---------------------------------------------------------
    call main

    # ---------------------------------------------------------
    # 3. Hang Loop (In case main returns)
    # ---------------------------------------------------------
loop:
    j loop